"""
Модуль контентной модерации свободного текста в анкете.

Политика (согласно ТЗ §7): при обнаружении стоп-слова поле free_text
очищается, но голос сохраняется. Отказа нет — ученик не знает о фильтрации.

Список стоп-корней хранится здесь как константа (~30 позиций).
Проверка регистронезависимая, ищем вхождение корня в тексте.
"""

# Корни нецензурных и оскорбительных слов (без окончаний — покрываем словоформы)
STOP_ROOTS: list[str] = [
    # Нецензурная лексика
    "хуй", "хуе", "хуя", "хую",
    "пизд",
    "ебат", "ебал", "ёбан", "ёбат", "еблан",
    "бляд", "блять",
    "залуп",
    "гандон",
    "дрочи", "дрочк",
    "трахн", "трахал",
    # Оскорбления
    "сука", "суки",
    "мудак", "мудил",
    "пидор", "пидар",
    "шлюх", "шалав",
    "мразь",
    "урод",
    "тварь",
    "ублюд",
    "скотин",
    "идиот",
    "кретин",
]


def has_stop_word(text: str) -> bool:
    """
    Проверить, содержит ли текст запрещённые слова.

    :param text: Произвольный текст для проверки.
    :return: True если найдено хотя бы одно стоп-слово, иначе False.

    Пример::
        >>> has_stop_word("всё хорошо")
        False
        >>> has_stop_word("это мудак написал")
        True
    """
    lowered = text.lower()
    return any(root in lowered for root in STOP_ROOTS)
